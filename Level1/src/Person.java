
public class Person{
		private String name;
		private int age;
		private String occupation;
		private double height;
		private String dateOfBirth;
		private int numberOfKids;
void setname(String n){
	name = n;
}String getname(){
	return name;
}
void setage(int a){
	age = a;
}int getage(){
	return age;
}
void setoccupation(String o){
	occupation = o;
}String getoccupation(){
	return occupation;
}
void setheight(double h){
	height = h;
}double getheight(){
	return height;
}
void setdateOfBirth(String d){
	dateOfBirth = d;
}String getdateOfBirth(){
	return dateOfBirth;
}
void setnumberOfKids(int k){
	numberOfKids = k;
}int getnumberOfKids(){
	return numberOfKids;
}
}